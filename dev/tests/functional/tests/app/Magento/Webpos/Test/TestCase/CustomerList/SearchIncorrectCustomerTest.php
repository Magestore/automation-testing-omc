namespace Magento\Webpos\Test\TestCase\CustomerList;

use Magento\Mtf\TestCase\Injectable;
use Magento\Webpos\Test\Fixture\Staff;
use Magento\Webpos\Test\Page\WebposIndex;

/**
 * Class SearchIncorrectCustomerTest
 * @package Magento\Webpos\Test\TestCase\CustomerList
 */
class SearchIncorrectCustomerTest extends Injectable
{
    /**
     * Webpos Index page.
     *
     * @var WebposIndex
     */
    protected $webposIndex;

    /**
     * Inject Webpos Login pages.
     *
     * @param WebposIndex $webposIndex
     * @return void
     */
    public function __inject(
        WebposIndex $webposIndex
    )
    {
        $this->webposIndex = $webposIndex;
    }

    /**
     * Login Webpos group test.
     *
     * @param Staff $staff
     * @return void
     */
    public function test(Staff $staff, $values)
    {
        $this->webposIndex->open();
        $this->webposIndex->getLoginForm()->fill($staff);
        $this->webposIndex->getLoginForm()->clickLoginButton();
        sleep(3);
        while ($this->webposIndex->getFirstScreen()->isVisible()) {}
        sleep(2);

        $this->webposIndex->getMsWebpos()->clickCMenuButton();
        $this->webposIndex->getCmenu()->customerList();
        foreach ($values as $value) {
            $this->webposIndex->getCustomerListContainer()->searchCustomer()->setValue($value);
            self::assertNotFalse(
                $this->webposIndex->getPageLayoutWebpos()->getErrorSearchText()->isVisible(),
                "Customer don't exist."
            );
        }
    }
}
